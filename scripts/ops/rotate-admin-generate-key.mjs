#!/usr/bin/env node

/**
 * Generate a fresh admin EOA secp256k1 key for the rotation.
 *
 * The private key is written to a single-purpose file under `.keys/`
 * (gitignored, mode 0600). The operator then provisions it into 1Password.
 *
 * Two provisioning paths, both keeping the key off any shell command line
 * (go-live P2 — "default to a non-echoing prompt; never placeholder-in-command"):
 *
 *   --write-to-op  (recommended): the script creates the 1Password item itself
 *       via `op item create`, passing the key as a DISCRETE argv element to
 *       execFile (no shell). The secret is never echoed, never a `<paste-here>`
 *       placeholder, and never enters shell history. Requires `op signin`.
 *
 *   default (manual): the operator runs the printed `op item create`, reading
 *       the concealed field straight from the 0600 file with `$(< file)` — a
 *       read-from-file, not a typed/pasted placeholder.
 *
 * Only the derived address is printed. The private key never reaches stdout,
 * stderr, process env, or a shell command line.
 *
 * Refuses to overwrite an existing key file (preserves prior generation for
 * evidence/rollback). If you want a different key, delete the file yourself
 * after confirming you have not used it.
 *
 * Usage:
 *   node scripts/ops/rotate-admin-generate-key.mjs                 # writes .keys/new-admin-eoa.txt
 *   node scripts/ops/rotate-admin-generate-key.mjs --out PATH      # custom path
 *   node scripts/ops/rotate-admin-generate-key.mjs --write-to-op   # also create the 1Password item (needs `op signin`)
 *   node scripts/ops/rotate-admin-generate-key.mjs --write-to-op --vault prod-critical --title admin-eoa-testnet
 */

import { Wallet } from "ethers";
import { existsSync, writeFileSync, chmodSync, mkdirSync } from "node:fs";
import { execFileSync } from "node:child_process";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(here, "..", "..");
const DEFAULT_OUT = resolve(repoRoot, ".keys", "new-admin-eoa.txt");
const DEFAULT_VAULT = "prod-critical";
const DEFAULT_TITLE = "admin-eoa-testnet";
const DEFAULT_CATEGORY = "API Credential";
const DEFAULT_CHAIN = "Paseo Asset Hub TestNet (chainId 420420417)";
const DEFAULT_NOTE = "Generated by rotate-admin-generate-key.mjs; replaces the prior admin EOA.";

function parseArgs(argv) {
  const args = {
    out: DEFAULT_OUT,
    vault: DEFAULT_VAULT,
    title: DEFAULT_TITLE,
    note: DEFAULT_NOTE,
    writeToOp: false
  };
  for (let i = 0; i < argv.length; i += 1) {
    const arg = argv[i];
    if (arg === "--out") args.out = resolve(argv[++i]);
    else if (arg === "--vault") args.vault = argv[++i];
    else if (arg === "--title") args.title = argv[++i];
    else if (arg === "--note") args.note = argv[++i];
    else if (arg === "--write-to-op") args.writeToOp = true;
    else if (arg === "--help" || arg === "-h") args.help = true;
  }
  return args;
}

function printUsage() {
  console.log(
    [
      "Usage: node scripts/ops/rotate-admin-generate-key.mjs [--out PATH] [--write-to-op]",
      "                                                      [--vault V] [--title T] [--note N]",
      "",
      "Writes a fresh secp256k1 private key to PATH (default .keys/new-admin-eoa.txt)",
      "with mode 0600. Prints the derived address only. Refuses to overwrite an",
      "existing file.",
      "",
      "--write-to-op  Also create the 1Password item via `op item create`, passing the",
      "               key as a discrete argv element (no shell, no echo, no placeholder,",
      "               no shell history). Requires `op signin` first."
    ].join("\n")
  );
}

/**
 * Build the argv array for `op item create`. The private key is a DISCRETE
 * element of the array — handed to execFile WITHOUT a shell, so it is never a
 * `$(...)` substitution, a `<paste-here>` placeholder, or a shell-history
 * entry. Exported so a test can assert that no-leak shape.
 */
export function buildOpItemCreateArgs({
  vault,
  title,
  category = DEFAULT_CATEGORY,
  address,
  privateKey,
  chain = DEFAULT_CHAIN,
  note = DEFAULT_NOTE
}) {
  if (!privateKey || !address) {
    throw new Error("buildOpItemCreateArgs requires both address and privateKey.");
  }
  return [
    "item",
    "create",
    "--vault",
    vault,
    "--category",
    category,
    "--title",
    title,
    `private key[concealed]=${privateKey}`,
    `address[text]=${address}`,
    `chain[text]=${chain}`,
    `notes[text]=${note}`
  ];
}

function opAvailable() {
  try {
    execFileSync("op", ["--version"], { stdio: "ignore" });
    return true;
  } catch {
    return false;
  }
}

function printManualSteps(args, wallet) {
  console.log("Provision into 1Password manually (key read from the 0600 file — never typed or pasted):");
  console.log("  1. op signin");
  console.log(`  2. op item create --vault ${args.vault} --category '${DEFAULT_CATEGORY}' \\`);
  console.log(`       --title '${args.title}' \\`);
  console.log(`       "private key[concealed]=$(< ${args.out})" \\`);
  console.log(`       'address[text]=${wallet.address}' \\`);
  console.log(`       'chain[text]=${DEFAULT_CHAIN}' \\`);
  console.log(`       'notes[text]=${args.note}'`);
  console.log("     (Or re-run with --write-to-op to skip this command entirely.)");
  console.log(`  3. Verify:  op read 'op://${args.vault}/${args.title}/address'   # expect: ${wallet.address}`);
  console.log(`  4. Delete the temp file:  rm ${args.out}`);
}

function main() {
  const args = parseArgs(process.argv.slice(2));
  if (args.help) {
    printUsage();
    return;
  }

  if (existsSync(args.out)) {
    console.error(
      `Refusing to overwrite ${args.out}.\n` +
        `Delete it yourself once you have confirmed the key it holds is not in use.`
    );
    process.exitCode = 1;
    return;
  }

  const dir = dirname(args.out);
  mkdirSync(dir, { recursive: true });
  try {
    chmodSync(dir, 0o700);
  } catch {
    // not fatal — best effort; the file mode itself is the real safety net
  }

  const wallet = Wallet.createRandom();
  writeFileSync(args.out, `${wallet.privateKey}\n`, { mode: 0o600, flag: "wx" });
  try {
    chmodSync(args.out, 0o600);
  } catch {}

  console.log("# rotate-admin-generate-key");
  console.log(`address:    ${wallet.address}`);
  console.log(`written to: ${args.out}  (mode 0600)`);
  console.log("");

  if (args.writeToOp) {
    if (!opAvailable()) {
      console.error("--write-to-op: `op` CLI not found on PATH. Falling back to manual steps.\n");
      printManualSteps(args, wallet);
      return;
    }
    try {
      // Key passed as a discrete argv element (no shell) — never echoed, never
      // a placeholder, never in shell history. stdout is dropped so op's item
      // echo can't surface anything; stderr is inherited for auth errors.
      execFileSync(
        "op",
        buildOpItemCreateArgs({
          vault: args.vault,
          title: args.title,
          address: wallet.address,
          privateKey: wallet.privateKey,
          note: args.note
        }),
        { stdio: ["ignore", "ignore", "inherit"] }
      );
    } catch (error) {
      console.error(
        `--write-to-op: op item create failed (${error?.message ?? error}). ` +
          "Are you signed in (op signin)? Falling back to manual steps.\n"
      );
      printManualSteps(args, wallet);
      return;
    }
    console.log(
      `Provisioned to 1Password: op://${args.vault}/${args.title}  ` +
        "(key passed via argv — never echoed, placed in a command placeholder, or written to shell history)."
    );
    console.log("Next steps for the operator:");
    console.log(`  1. Verify:  op read 'op://${args.vault}/${args.title}/address'   # expect: ${wallet.address}`);
    console.log(`  2. Delete the temp file:  rm ${args.out}`);
    return;
  }

  printManualSteps(args, wallet);
}

if (import.meta.url === `file://${process.argv[1]}`) {
  main();
}
