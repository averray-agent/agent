/**
 * render-mainnet-backend-env.mjs
 *
 * GAP script (critical-path item "mainnet backend env profile [Claude]"). Derives
 * the mainnet op-inject templates from the committed testnet templates by applying
 * a documented delta transform, instead of hand-copying 388 lines of credential
 * template (which rots). Re-run with --check in CI to catch drift: if the testnet
 * template changes and the mainnet one isn't regenerated, the check fails.
 *
 * It writes:
 *   deploy/backend.mainnet.env.template
 *   deploy/indexer.mainnet.env.template
 * and regenerates the "mainnet-generated" block of deploy/secrets-inventory.md so
 * every mainnet op:// ref has a matching inventory row (the structural lint requires
 * this). No secret VALUES are ever produced — only op:// references; rendering happens
 * later on the VPS via `op inject` once the mainnet-* vault items exist.
 *
 * The delta rules (documented, so a reviewer can audit the diff):
 *   • identity literals → mainnet: AUTH_CHAIN_ID=420420419, RPC endpoints → eth-rpc.polkadot.io
 *   • every op://prod-* → op://mainnet-* (and the "-testnet" item slug → "-mainnet")
 *   • REMOVE (not blank): AUTH_JWT_SECRETS (HMAC retired), ARBITRATOR_SIGNER_PRIVATE_KEY
 *     (arbitrator is keyless on mainnet — the 3 coupled backend edits are Codex-owned)
 *   • ADD: SHARE_URL_SECRET as a dedicated op:// ref (the testnet HMAC fallback is gone)
 *   • per-deploy unknowns → commented TODO(operator): the 5 contract addresses, the
 *     admin/verifier allowlists, the ponder addresses/start-blocks, the DB schema
 *   • launch posture: INGESTION_PREFUND_ENABLED=false, USDC_LIQUIDITY_CHAIN=mainnet
 *   • KEEP: SIGNER_BACKEND=kms, JWT_BACKEND=kms, TOKEN_ADDRESS (USDC precompile),
 *     SUPPORTED_ASSETS_JSON, JWT_MAX_TTL_SECONDS (⚠ DEC-4 must drop to <=1h before launch)
 *
 * Usage:
 *   node scripts/ops/render-mainnet-backend-env.mjs           # write the files
 *   node scripts/ops/render-mainnet-backend-env.mjs --check    # CI: fail if committed output is stale
 */

