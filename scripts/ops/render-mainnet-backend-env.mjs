/** Repoint an op:// value: prod-* vault → mainnet-*, and normalize the item slug. */
export function repointOpRef(value) {
  if (!value.startsWith("op://")) return value;
  const rest = value.slice("op://".length);
  const segs = rest.split("/");
  const [vault, item, ...field] = segs;
  const newVault = vault.replace(/^prod-/, "mainnet-");
  const newItem = (item || "").replace(/testnet/g, "mainnet");
  return `op://${[newVault, newItem, ...field].join("/")}`;
}

const DROP = Symbol("drop-line");

/** Transform a single template line. Returns the new line, or DROP to remove it. */
export function transformLine(line) {
  const m = line.match(/^([A-Z][A-Z0-9_]*)\s*=\s*(.*)$/);
  if (!m) return line; // comment / blank / non-assignment — keep verbatim
  let [, key, value] = m;

  // Ponder's RPC key carries the chain id: PONDER_RPC_URL_420420417 → ..._420420419
  if (key === `PONDER_RPC_URL_${TESTNET_CHAIN_ID}`) {
    return `PONDER_RPC_URL_${MAINNET_CHAIN_ID}=${MAINNET_RPC}`;
  }
  if (REMOVE_KEYS.has(key)) return DROP;
  if (Object.prototype.hasOwnProperty.call(LITERAL_OVERRIDES, key)) return `${key}=${LITERAL_OVERRIDES[key]}`;
  if (Object.prototype.hasOwnProperty.call(TODO_KEYS, key)) return `# ${key}=  TODO(operator): ${TODO_KEYS[key]}`;
  if (value.startsWith("op://")) return `${key}=${repointOpRef(value)}`;
  if (value === TESTNET_RPC) return `${key}=${MAINNET_RPC}`;
  return line;
}

/** Transform a whole template body. */
export function transformTemplate(source, sourcePath, { additions = [] } = {}) {
  const out = [...HEADER(sourcePath)];
  for (const line of source.split("\n")) {
    const t = transformLine(line);
    if (t !== DROP) out.push(t);
  }
  if (additions.length) out.push(...additions);
  // collapse any accidental leading blank duplication, ensure single trailing newline
  return out.join("\n").replace(/\n{3,}/g, "\n\n").replace(/\s*$/, "") + "\n";
}

/** Collect the op:// refs (VAR + path) an already-rendered template exposes. */
export function collectOpRefs(rendered) {
  const refs = [];
  for (const line of rendered.split("\n")) {
    const m = line.match(/^([A-Z][A-Z0-9_]*)\s*=\s*(op:\/\/\S+)\s*$/);
    if (m) refs.push({ varName: m[1], path: m[2] });
  }
  return refs;
}

/** Build the generated inventory markdown block from the collected refs. */
export function buildInventoryBlock(refs) {
  const rows = refs.map(
    (r) =>
      `| \`${r.varName}\` | \`${r.path}\` | see prod-* row | deployer | Mainnet mirror — generated; rotation detail on the matching prod-* row. |`
  );
  return [
    GEN_BEGIN,
    "",
    "## Mainnet runtime secrets (generated — do not hand-edit)",
    "",
    "Regenerated by `scripts/ops/render-mainnet-backend-env.mjs`. Each row is the",
    "mainnet mirror of a prod-* item, read by the mainnet VPS/CI service-account",
    "tokens minted via `scripts/ops/bootstrap-mainnet-vault.mjs`.",
    "",
    "| Env var | `op://` path | Critical-nonempty | Rotation owner | Notes |",
    "| ------- | ------------ | :---------------: | -------------- | ----- |",
    ...rows,
    "",
    GEN_END,
  ].join("\n");
}

/** Splice the generated block into the inventory doc (append if markers absent). */
export function spliceInventory(existing, block) {
  const b = existing.indexOf(GEN_BEGIN);
  const e = existing.indexOf(GEN_END);
  if (b !== -1 && e !== -1 && e > b) {
    return existing.slice(0, b) + block + existing.slice(e + GEN_END.length);
  }
  return existing.replace(/\s*$/, "") + "\n\n" + block + "\n";
}

const OUTPUTS = [
  { src: "deploy/backend.env.template", out: "deploy/backend.mainnet.env.template", additions: BACKEND_ADDITIONS },
  { src: "deploy/indexer.env.template", out: "deploy/indexer.mainnet.env.template", additions: [] },
];
const INVENTORY = "deploy/secrets-inventory.md";

/** Produce every generated artifact in-memory: templates + the inventory doc. */
export function generateAll(readFile = (p) => readFileSync(join(REPO_ROOT, p), "utf8")) {
  const files = {};
  const allRefs = [];
  for (const { src, out, additions } of OUTPUTS) {
    const rendered = transformTemplate(readFile(src), src, { additions });
    files[out] = rendered;
    allRefs.push(...collectOpRefs(rendered));
  }
  files[INVENTORY] = spliceInventory(readFile(INVENTORY), buildInventoryBlock(allRefs));
  return files;
}

async function main() {
  const check = process.argv.slice(2).includes("--check");
  const files = generateAll();
  const drift = [];
  for (const [rel, content] of Object.entries(files)) {
    const abs = join(REPO_ROOT, rel);
    let current = "";
    try {
      current = readFileSync(abs, "utf8");
    } catch {
      current = null;
    }
    if (check) {
      if (current !== content) drift.push(rel);
    } else {
      if (current !== content) {
        writeFileSync(abs, content);
        console.log(`  wrote ${rel}`);
      } else {
        console.log(`  ok    ${rel} (unchanged)`);
      }
    }
  }
  if (check) {
    if (drift.length) {
      console.error(`render-mainnet-backend-env --check: STALE — regenerate:\n  - ${drift.join("\n  - ")}`);
      console.error(`  run: node scripts/ops/render-mainnet-backend-env.mjs`);
      process.exit(1);
    }
    console.log("render-mainnet-backend-env --check: ok (committed output matches generator)");
  }
}

const isCli = process.argv[1] && process.argv[1].endsWith("render-mainnet-backend-env.mjs");
if (isCli) {
  main().catch((err) => {
    console.error(err?.message ?? err);
    process.exit(1);
  });
}
